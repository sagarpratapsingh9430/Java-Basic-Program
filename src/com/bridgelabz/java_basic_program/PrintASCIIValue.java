package com.bridgelabz.java_basic_program;

public class PrintASCIIValue {
    public static void main(String[] args) {
        char ch1 = 'A';
        char ch2 = 'a';
        char ch3 = 'Z';
        char ch4 = 'z';
        int x1 = ch1;
        int x2 = ch2;
        int x3 = ch3;
        int x4 = ch4;
        System.out.println(x1);
        System.out.println(x2);
        System.out.println(x3);
        System.out.println(x4);
    }
}
